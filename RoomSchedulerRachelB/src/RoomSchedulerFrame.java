    private void dropRoomButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropRoomButtonActionPerformed
        String room = dropRoomComboBox.getSelectedItem().toString();
        
        ArrayList<ReservationEntry> reservations = ReservationQueries.getReservationEntry();
        for (ReservationEntry entry : reservations)
        {
            if(room.equals(entry.getRoom())){
                ReservationQueries.cancelReservation(entry.getName(), entry.getDate());
                WaitlistQueries.addWaitlistEntry(entry.getName(), entry.getDate(), entry.getSeats(), entry.getTimestamp());
            }
        }
        dropRoomText.setText("Room "+room+" has been dropped from the database");
        RoomQueries.dropRoom(room);
        rebuildRoomComboBoxes();
    }//GEN-LAST:event_dropRoomButtonActionPerformed

