    private void reserveSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reserveSubmitButtonActionPerformed
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        int seats = Integer.parseInt(reserveSeatsTextField.getText());
        Date date = Date.valueOf((String) reserveDateJComboBox.getSelectedItem());
        String name =reserveFacultyComboBox.getSelectedItem().toString();
        ArrayList<String> totalRooms = RoomQueries.getAllPossibleRooms(seats);
        ArrayList<String> reservedRooms = ReservationQueries.getRoomsReservedByDate(date);
        String setRoom = null;
        for (String room: totalRooms){
            if (!reservedRooms.contains(room))
            {
                if(setRoom == null){
                    setRoom = room;
                }
                else if(RoomQueries.getSeatsfromRoom(room) < RoomQueries.getSeatsfromRoom(setRoom)){
                    setRoom = room;
                }
                else{
                    continue;
                    }
            }
            else{
                continue;
            }
        }
        
        if(setRoom != null){  
            ReservationQueries.addReservationEntry(name, setRoom, date, seats, currentTimestamp);
            reserveStatusLabel.setText(name+"'s reservation on " + date + " in room " + setRoom + " had been added.");
            return ;
        }
        else{
            WaitlistQueries.addWaitlistEntry(name, date, seats, currentTimestamp);//adds to waitlistTextField
            reserveStatusLabel.setText(name+"'s reservation on "+date+" for "+seats+" seats has been added to the waitlist.");
            return ;
        
        }
    }//GEN-LAST:event_reserveSubmitButtonActionPerformed

