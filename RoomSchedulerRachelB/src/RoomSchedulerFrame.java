    private void addRoomButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addRoomButtonActionPerformed
        // TODO add your handling code here:
        String name = roomNameTextField.getText();
        int seats = Integer.parseInt(roomSeatTextField.getText());
        RoomQueries.addRoom(name, seats);
        
        //loop through dates in the date database check if on waitlist
        //then check the number of seats in waitlist and if seats is less than room seats, add to reservation
        ArrayList<String> dates = Dates.getAllDates();
        
        for(String date: dates){
            ArrayList<WaitlistEntry> entries = WaitlistQueries.getWaitlistByDate(Date.valueOf(date));
            for(WaitlistEntry entry: entries){
                if(seats>=entry.getSeats()){
                    ReservationQueries.addReservationEntry(entry.getFaculty(), name, entry.getDate(), entry.getSeats(), entry.getTimestamp());
                    WaitlistQueries.cancelWaitlistEntry(entry.getFaculty(), entry.getDate());
                    addRoomTextField.setText("Room "+name+" has been added to the database.");
                    return ;
                }
            }
        }
        addRoomTextField.setText("Room "+name+" has been added to the database.");
        rebuildRoomComboBoxes();
        
    }//GEN-LAST:event_addRoomButtonActionPerformed

